private void saveAccountToCSV(AccountInfo acc) {

    String filePath = "accounts.csv";  // You can change directory if needed

    try (FileWriter fw = new FileWriter(filePath, true);
         BufferedWriter bw = new BufferedWriter(fw);
         PrintWriter out = new PrintWriter(bw)) {

        // Write data in CSV format
        out.println(acc.accNo + "," +
                    acc.name + "," +
                    acc.initialBalance + "," +
                    acc.currentBalance + "," +
                    acc.totalDeposit + "," +
                    acc.totalWithdraw);

    } catch (IOException e) {
        e.printStackTrace();
    }
}

private boolean accountExistsInCSV(String accNumber) {

    String filePath = "accounts.csv";

    try (java.io.BufferedReader br = new java.io.BufferedReader(new java.io.FileReader(filePath))) {
        String line;

        while ((line = br.readLine()) != null) {
            String[] data = line.split(",");

            if (data.length > 0 && data[0].equals(accNumber)) {
                return true;  // ACCOUNT FOUND
            }
        }

    } catch (IOException e) {
        // File might not exist yet â†’ no accounts saved
        return false;
    }

    return false;
}


    
    private void btnCreateAccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateAccActionPerformed


    String accNumber = txtAccNo.getText();
    String name = txtName.getText();
    double amount = Double.parseDouble(txtInitialBalance.getText());

    // 1. CHECK in CSV file
    if (accountExistsInCSV(accNumber)) {
        JOptionPane.showMessageDialog(this,
                "This Account Number already exists in the system!",
                "Duplicate Account Number",
                JOptionPane.ERROR_MESSAGE);
        return;  // STOP
    }

    AccountInfo acc = new AccountInfo(accNumber, name, amount, amount, 0, 0);

    // 2. Save in HashMap (for current runtime)
    AccountInfo.accounts.put(accNumber, acc);

    // 3. Save to CSV
    saveAccountToCSV(acc);

    // 4. Refresh admin table
    adminPanel.refreshAdminTable();

    JOptionPane.showMessageDialog(this, "Account Created Successfully!");





    }//GEN-LAST:event_btnCreateAccActionPerformed

    
    
    
